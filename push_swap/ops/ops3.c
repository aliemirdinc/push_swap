#include "../push_swap.h"

void	op_rrr(t_stack **a, t_stack **b, int i)
{
	if (!(*a) || !((*a)->next) || !(*b) || !((*b)->next))
		return ;
	op_rra(a, i + 42);
	op_rrb(b, i + 42);
	if (i == 0)
		ft_printf("rrr\n");
}
